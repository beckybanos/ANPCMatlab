%x=.5;
%y=-.1*(x+h)^4-.15*(x+h)^5-.5*(x+h)^2-.25*(x+h)+1.2;
%z=-.1*(x-h)^4-.15*(x-h)^5-.5*(x-h)^2-.25*(x-h)+1.2;
n=11;
for i=1:n
    h=(h/2);
    syms x;
    f(x)=-.1*x^4-.15*x^5-.5*x^2-.25*x+1.2;

    f2(x)=(f(x+h)-f(x-h))/(2*h);

    g(x)=diff(f,x);
    g(.5);
    f2(.5);
end

